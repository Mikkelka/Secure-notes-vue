<template>
  <div class="fullscreen-bg">
    <div class="bg-gray-800/80 backdrop-blur border border-gray-700/50 rounded-2xl p-6 w-full max-w-md">
      <div class="text-center mb-6">
        <div class="icon-container mb-3">
          <Shield class="w-6 h-6 text-white" />
        </div>
        <h1 class="text-2xl font-bold text-white mb-1">SecureNotes</h1>
        <p class="text-gray-400 text-sm">End-to-end krypterede noter</p>
    
      </div>

      <!-- Google Login - Primary option -->
      <div class="mb-6">
        <button
          @click="handleGoogleSubmit"
          :disabled="loading"
          class="btn-google"
          style="background-color: #ffffff; color: #3c4043; border: 1px solid #dadce0; font-family: 'Google Sans', Roboto, sans-serif;"
        >
          <div v-if="loading" class="w-5 h-5 border-2 border-gray-400 border-t-blue-500 rounded-full animate-spin" />
          <svg v-else class="w-5 h-5" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          Log ind med Google
        </button>
      </div>

      <!-- Divider -->
      <div class="flex items-center gap-3 mb-6">
        <div class="flex-1 h-px bg-gray-600"></div>
        <span class="text-gray-400 text-sm">eller brug email</span>
        <div class="flex-1 h-px bg-gray-600"></div>
      </div>

      <div class="space-y-3">
        <div class="relative">
          <input
            v-model="email"
            type="email"
            placeholder="Email"
            class="input-base"
          />
        </div>
        
        <div class="relative">
          <input
            v-model="password"
            :type="showPassword ? 'text' : 'password'"
            placeholder="Password (bruges til kryptering)"
            class="input-base pr-11"
            @keypress.enter="handleSubmit('login')"
          />
          <button
            type="button"
            @click="showPassword = !showPassword"
            class="absolute right-3 top-1/2 transform -translate-y-1/2 text-gray-400 hover:text-white transition-colors"
          >
            <EyeOff v-if="showPassword" class="w-4 h-4" />
            <Eye v-else class="w-4 h-4" />
          </button>
        </div>

        <!-- Error message -->
        <div v-if="error" class="flex items-center gap-2 p-3 bg-red-500/10 border border-red-500/20 rounded-lg text-red-300 text-sm">
          <AlertCircle class="w-4 h-4 flex-shrink-0" />
          <span>{{ error }}</span>
        </div>

        <div class="flex gap-2 pt-2">
          <BaseButton
            @click="handleSubmit('login')"
            :disabled="!email || !password"
            :loading="loading"
            variant="primary"
            class="flex-1"
          >
            <Unlock v-if="!loading" class="w-4 h-4" />
            Login
          </BaseButton>
          <BaseButton
            @click="handleSubmit('register')"
            :disabled="!email || !password"
            :loading="loading"
            variant="secondary"
            class="flex-1"
          >
            <Plus v-if="!loading" class="w-4 h-4" />
            Registrer
          </BaseButton>
        </div>
      </div>

      <div class="mt-4 space-y-3">
        <div class="p-3 bg-blue-500/10 border border-blue-500/20 rounded-lg">
          <div class="flex items-center gap-2 text-blue-300 text-sm">
            <Lock class="w-4 h-4" />
            <span>Dine data krypteres med dit password f√∏r lagring</span>
          </div>
        </div>
        
        <div class="p-3 bg-green-500/10 border border-green-500/20 rounded-lg">
          <div class="flex items-center gap-2 text-green-300 text-sm">
            <Shield class="w-4 h-4" />
            <span>End-to-end kryptering med AES-256-GCM</span>
          </div>
        </div>
      </div>
      
      <div class="text-center mt-4">
        <p class="text-gray-500 text-xs">v1.0.0</p>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref } from 'vue'
import { Shield, Unlock, Plus, Eye, EyeOff, Lock, AlertCircle } from 'lucide-vue-next'
import BaseButton from '../base/BaseButton.vue'

defineProps({
  loading: {
    type: Boolean,
    default: false
  }
})

const emit = defineEmits(['login', 'register', 'googleLogin'])

const email = ref('')
const password = ref('')
const showPassword = ref(false)
const error = ref('')

const handleSubmit = async (action) => {
  error.value = ''
  
  if (action === 'login') {
    emit('login', email.value, password.value)
  } else {
    emit('register', email.value, password.value)
  }
}

const handleGoogleSubmit = async () => {
  error.value = ''
  emit('googleLogin')
}

// Expose method to set error from parent
const setError = (errorMessage) => {
  error.value = errorMessage
}

const clearForm = () => {
  email.value = ''
  password.value = ''
  error.value = ''
}

defineExpose({
  setError,
  clearForm
})
</script>